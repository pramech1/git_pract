package Array_Practice;

public class Arrays_char 
{

	public static void main(String[] args)
	{
		
		
		// Declare the char type array
		
		char[] ch = new char[4];
		
		// Initialize the array
		
		ch[0]='A';
		ch[1]='B';
		ch[2]='C';
		ch[3]='D';
		
		// usage 
		
		for(int i=0 ; i<=ch.length-1 ; i++)
		{
			System.out.println(ch[i]);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}

