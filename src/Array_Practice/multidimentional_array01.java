package Array_Practice;

public class multidimentional_array01
{

	public static void main(String[] args)
	{
		
		
//		0     1     2
//	0   10    20    30
//	1   40    50    60
		
		int[][] ar = new int[][]{{10,20,30},{40,50,60}};
		
		
	//	int[][] ar1 = new int[][] {{10,20,30},{40,50,60}};
		
		for(int i=0; i<=1; i++)
		{
			for(int a=0; a<=2 ; a++)
			{
				System.out.print(ar[i][a] + " ");
			}
			System.out.println();
		}
		
		
		

		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
