
public class PErfect_number 
{
	
	
	public static void main(String[] args)
	{
		
		
		int n =100 , sum = 0;
		
	for  (int i = 1 ; i < n ; i++)
		{
			if (n % i == 0)
			{
			   sum = sum + i;
			}	
		}
		
		if(n== sum)
		{
			System.out.println(n +" "+ "Number is Perfect");
		}
		
		else
		{
			System.out.println(n +" "+ "Number is not Perfect");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
