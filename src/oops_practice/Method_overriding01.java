package oops_practice;

public class Method_overriding01 
{
	
	// declare the methods 
	
	
	public static void Moo()
	{
		System.out.println("How r u Pranav");
	}
	
	public static void Moo(int a)    // method overloaded  Moo 
	{
		System.out.println(a);
	}
	
	
//-----------------------------------------------------------------------------------------------------
	
	
	// declare the Non static methods 
	
	public void Moo1()
	{
		System.out.println("Hi Everyone ");
	}
	
	public void Moo1(char sgrade)     // Method overloaded Moo1
	{
		System.out.println("Hello I am from Super class");
	}
	
	
	
	
	
	

}
